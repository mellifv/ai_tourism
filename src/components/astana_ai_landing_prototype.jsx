// AI-powered itinerary generator
async function generateItineraryData(query = '', lang = 'en') {
  try {
    // IMPORTANT: Use the correct endpoint
    const API_ENDPOINT = '/api/gemini'; // Changed from /api/free-ai
    
    // Simplified prompt that works better
    const prompts = {
      en: `Create a one-day Astana itinerary for: "${query}". 
           Format as JSON: {
             "title": "string",
             "items": [
               {"time": "9:00 AM", "place": "Place Name", "cost": "5000‚Ç∏", "description": "Brief description"}
             ]
           }
           Total budget: 400,000‚Ç∏ max.`,
      ru: `–°–æ–∑–¥–∞–π –æ–¥–Ω–æ–¥–Ω–µ–≤–Ω—ã–π –º–∞—Ä—à—Ä—É—Ç –ø–æ –ê—Å—Ç–∞–Ω–µ –¥–ª—è: "${query}".
           –§–æ—Ä–º–∞—Ç JSON: {
             "title": "string",
             "items": [
               {"time": "9:00", "place": "–ù–∞–∑–≤–∞–Ω–∏–µ –º–µ—Å—Ç–∞", "cost": "5000‚Ç∏", "description": "–ö—Ä–∞—Ç–∫–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ"}
             ]
           }
           –ë—é–¥–∂–µ—Ç: –º–∞–∫—Å–∏–º—É–º 400,000‚Ç∏.`,
      kz: `–ê—Å—Ç–∞–Ω–∞“ì–∞ –±—ñ—Ä –∫“Ø–Ω–¥—ñ–∫ –º–∞—Ä—à—Ä—É—Ç –∂–∞—Å–∞: "${query}".
           JSON –ø—ñ—à—ñ–º—ñ: {
             "title": "string",
             "items": [
               {"time": "9:00", "place": "–û—Ä—ã–Ω –∞—Ç–∞—É—ã", "cost": "5000‚Ç∏", "description": "“ö—ã—Å“õ–∞ —Å–∏–ø–∞—Ç—Ç–∞–º–∞"}
             ]
           }
           –ë—é–¥–∂–µ—Ç: –µ“£ –∫”©–±—ñ 400,000‚Ç∏.`
    };

    console.log('üì§ Sending request to:', API_ENDPOINT);
    console.log('üìù Prompt:', prompts[lang].substring(0, 100) + '...');
    
    const response = await fetch(API_ENDPOINT, {
      method: 'POST',
      headers: { 
        'Content-Type': 'application/json',
        'Accept': 'application/json'
      },
      body: JSON.stringify({ 
        prompt: prompts[lang]
      })
    });

    console.log('üì• Response status:', response.status);
    
    if (!response.ok) {
      const errorText = await response.text();
      console.error('‚ùå Server error response:', errorText);
      throw new Error(`Server error ${response.status}`);
    }

    const data = await response.json();
    console.log('üì¶ Response data:', data);
    
    if (!data.success) {
      throw new Error(data.error || 'API request failed');
    }
    
    const aiText = data.response;
    console.log('ü§ñ AI Response:', aiText);
    
    // Try to extract JSON
    let jsonMatch = aiText.match(/```json\n([\s\S]*?)\n```/) || 
                   aiText.match(/\{[\s\S]*\}/);
    
    if (!jsonMatch) {
      console.error('No JSON found in:', aiText);
      throw new Error('No valid JSON in AI response');
    }
    
    const jsonStr = jsonMatch[0].replace(/```json\n|\n```/g, '');
    const aiResponse = JSON.parse(jsonStr);

    if (aiResponse.title && aiResponse.items && Array.isArray(aiResponse.items)) {
      // Calculate total
      const total = aiResponse.items.reduce((acc, item) => {
        const cost = parseInt(item.cost?.replace(/[^0-9]/g, '')) || 0;
        return acc + cost;
      }, 0);

      return {
        title: aiResponse.title,
        items: aiResponse.items,
        total: `${total.toLocaleString()}‚Ç∏`,
        source: 'ai'
      };
    } else {
      throw new Error('Invalid itinerary format');
    }

  } catch (error) {
    console.error('üí• API error:', error);
    throw new Error(`Failed to generate: ${error.message}`);
  }
}

// PDF Download function
function downloadPDF(itinerary, lang = 'en') {
  import('html2pdf.js').then((html2pdf) => {
    const translations = {
      en: { totalCost: 'Total Estimated Cost', enjoy: 'Enjoy your day in Astana! üéâ', generatedOn: 'Generated on' },
      ru: { totalCost: '–û–±—â–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å', enjoy: '–ü—Ä–∏—è—Ç–Ω–æ–≥–æ –¥–Ω—è –≤ –ê—Å—Ç–∞–Ω–µ! üéâ', generatedOn: '–°–æ–∑–¥–∞–Ω–æ' },
      kz: { totalCost: '–ñ–∞–ª–ø—ã –±–∞“ì–∞—Å—ã', enjoy: '–ê—Å—Ç–∞–Ω–∞–¥–∞“ì—ã –∫“Ø–Ω—ñ“£—ñ–∑ —Å”ô—Ç—Ç—ñ ”©—Ç—Å—ñ–Ω! üéâ', generatedOn: '–ñ–∞—Å–∞–ª“ì–∞–Ω –∫“Ø–Ω—ñ' }
    };
    const t = translations[lang];

    const content = `
      <div style="font-family: Arial, sans-serif; padding: 20px; max-width: 800px; margin: 0 auto;">
        <div style="text-align: center; margin-bottom: 30px; border-bottom: 2px solid #00bcd4; padding-bottom: 20px;">
          <h1 style="font-size: 28px; font-weight: bold; color: #333; margin: 0 0 10px 0;">${itinerary.title}</h1>
          <div style="font-size: 18px; color: #666; margin: 10px 0;">${t.totalCost}: ${itinerary.total}</div>
          <div style="color: #888; font-size: 14px;">Generated by Astana AI Trips</div>
        </div>
        
        ${itinerary.items.map((item) => `
          <div style="margin: 20px 0; padding: 15px; border-left: 4px solid #00bcd4; background: #f9f9f9;">
            <div style="display: flex; justify-content: between; align-items: start; margin-bottom: 8px;">
              <span style="font-weight: bold; color: #0288d1; font-size: 16px; min-width: 60px;">${item.time}</span>
              <div style="flex: 1;">
                <div style="font-size: 18px; font-weight: bold; color: #333; margin-bottom: 5px;">${item.place}</div>
                ${item.description ? `<div style="font-size: 14px; color: #555; line-height: 1.4;">${item.description}</div>` : ''}
              </div>
              <span style="font-weight: bold; color: #d32f2f; font-size: 16px; margin-left: 15px;">${item.cost}</span>
            </div>
          </div>
        `).join('')}
        
        <div style="text-align: center; margin-top: 40px; padding-top: 20px; border-top: 1px solid #ddd; color: #888; font-size: 14px;">
          <p>${t.enjoy}</p>
          <p>${t.generatedOn} ${new Date().toLocaleDateString()}</p>
        </div>
      </div>
    `;

    const element = document.createElement('div');
    element.innerHTML = content;
    document.body.appendChild(element);

    const options = {
      margin: 10,
      filename: `Astana-Itinerary-${new Date().getTime()}.pdf`,
      image: { type: 'jpeg', quality: 0.98 },
      html2canvas: { scale: 2, useCORS: true },
      jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' }
    };

    html2pdf.default().set(options).from(element).save().then(() => {
      document.body.removeChild(element);
    });
  }).catch(error => console.error('Error loading html2pdf:', error));
}

